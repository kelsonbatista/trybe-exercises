function checkFruits(myBasket) {

  let fruitsList = {};

  for(let i = 0; i < myBasket.length; i++) {
    let fruitsItem = myBasket[i];
    if(fruitsList[fruitsItem]) {
      fruitsList[fruitsItem] += 1;
    } else {
      fruitsList[fruitsItem] = 1;
    }
  }
  // retirado do gabarito a parte da mensagem
  let message = [];
  for(let fruit in fruitsList) {
    let text = `${fruitsList[fruit]} ${fruit}`;
    if (fruitsList[fruit] > 1) {
      text += 's';
    }
    message.push(text);
  }
  return (`Sua cesta possui: ${message.join(', ')}.`);
}

